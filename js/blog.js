document.addEventListener('DOMContentLoaded', () => {
  const blogContainer = document.querySelector('.blog-render-target');
  const searchInput = document.querySelector('#blog-search');
  const catItems = document.querySelectorAll('.blog-cat-filter');
  const blogModal = document.querySelector('#course-modal'); // Reusing detail modal overlay structure for clean code

  // Initial load
  if (blogContainer) {
    renderBlogs(BLOG_DATA);

    // Live search
    if (searchInput) {
      searchInput.addEventListener('input', () => {
        filterAndRenderBlogs();
      });
    }

    // Sidebar Category clicks
    catItems.forEach(item => {
      item.addEventListener('click', (e) => {
        e.preventDefault();
        catItems.forEach(i => i.classList.remove('active'));
        item.classList.add('active');
        filterAndRenderBlogs();
      });
    });
  }

  function filterAndRenderBlogs() {
    let filtered = BLOG_DATA;
    const query = searchInput ? searchInput.value.toLowerCase() : '';
    
    // Category Filter
    const activeCatEl = document.querySelector('.blog-cat-filter.active');
    const category = activeCatEl ? activeCatEl.getAttribute('data-filter') : 'all';

    if (category !== 'all') {
      filtered = filtered.filter(b => b.category === category);
    }

    // Search query
    if (query !== '') {
      filtered = filtered.filter(b => 
        b.title.toLowerCase().includes(query) || 
        b.excerpt.toLowerCase().includes(query) ||
        b.content.toLowerCase().includes(query)
      );
    }

    renderBlogs(filtered);
  }

  function renderBlogs(data) {
    if (!blogContainer) return;

    if (data.length === 0) {
      blogContainer.innerHTML = `
        <div style="text-align: center; padding: 4rem 0;">
          <h3 style="font-size: 1.5rem; color: var(--neutral-gray);">No articles found.</h3>
          <p style="color: var(--neutral-gray); margin-top: 0.5rem;">Try modifying your search or select another category.</p>
        </div>
      `;
      return;
    }

    blogContainer.innerHTML = data.map(post => `
      <article class="blog-post-card reveal active">
        <div class="blog-post-img-box">
          <img src="${post.image}" alt="${post.title}" style="width: 100%; height: 100%; object-fit: cover;">
        </div>
        <div class="blog-post-content">
          <div class="blog-post-meta">
            <span>📅 ${post.date}</span>
            <span>👤 By ${post.author}</span>
            <span>⏱️ ${post.readTime}</span>
          </div>
          <h2 class="blog-post-title">${post.title}</h2>
          <p class="blog-post-excerpt">${post.excerpt}</p>
          <a href="#" class="btn btn-secondary read-article-btn" data-id="${post.id}">Read Full Article</a>
        </div>
      </article>
    `).join('');

    // Attach click triggers to open article reader
    const readBtns = blogContainer.querySelectorAll('.read-article-btn');
    readBtns.forEach(btn => {
      btn.addEventListener('click', (e) => {
        e.preventDefault();
        const id = btn.getAttribute('data-id');
        openArticle(id);
      });
    });
  }

  function openArticle(id) {
    const post = BLOG_DATA.find(p => p.id === id);
    if (!post || !blogModal) return;

    const modalBox = blogModal.querySelector('.course-modal-box');
    modalBox.innerHTML = `
      <span class="course-modal-close">&times;</span>
      <span class="badge badge-primary">${post.category.toUpperCase()}</span>
      <h2 class="course-modal-title" style="margin-top: 0.5rem; line-height: 1.25;">${post.title}</h2>
      
      <div style="display: flex; gap: 1.5rem; font-size: 0.85rem; color: var(--neutral-gray); margin-bottom: 2rem; border-bottom: 1px solid var(--neutral-border); padding-bottom: 1rem; font-weight: 500;">
        <span>📅 ${post.date}</span>
        <span>👤 Written by: <strong>${post.author}</strong></span>
        <span>⏱️ Reading speed: ${post.readTime}</span>
      </div>

      <div class="blog-featured-img" style="width: 100%; height: 300px; border-radius: var(--radius-md); overflow: hidden; margin-bottom: 2rem;">
        <img src="${post.image}" alt="${post.title}" style="width: 100%; height: 100%; object-fit: cover;">
      </div>
      
      <div class="blog-full-article-content" style="color: var(--neutral-dark); line-height: 1.7; font-size: 1.05rem;">
        ${post.content}
      </div>

      <div style="margin-top: 3rem; border-top: 1px solid var(--neutral-border); padding-top: 2rem; display: flex; align-items: center; gap: 1rem;">
        <div style="width: 48px; height: 48px; border-radius: var(--radius-full); background: var(--primary-light); display: flex; align-items: center; justify-content: center; font-size: 1.5rem;">🎓</div>
        <div>
          <strong style="display: block;">Looking to build dynamic computer skills?</strong>
          <span style="font-size: 0.85rem; color: var(--neutral-gray);">Join Vikash Sahu's classes at ABC Computer Training Centre for practical career training.</span>
        </div>
        <a href="admission.html" class="btn btn-primary" style="margin-left: auto; font-size: 0.85rem; padding: 0.6rem 1.2rem;">Apply Now</a>
      </div>
    `;

    // Apply basic typography styles to modal article content
    const headings = modalBox.querySelectorAll('.blog-full-article-content h3');
    headings.forEach(h => {
      h.style.fontSize = '1.35rem';
      h.style.marginTop = '1.5rem';
      h.style.marginBottom = '0.75rem';
    });

    const paragraphs = modalBox.querySelectorAll('.blog-full-article-content p');
    paragraphs.forEach(p => {
      p.style.marginBottom = '1.25rem';
    });

    const lists = modalBox.querySelectorAll('.blog-full-article-content ul');
    lists.forEach(l => {
      l.style.paddingLeft = '1.5rem';
      l.style.marginBottom = '1.5rem';
    });

    const listItems = modalBox.querySelectorAll('.blog-full-article-content li');
    listItems.forEach(li => {
      li.style.marginBottom = '0.5rem';
    });

    blogModal.classList.add('active');
    document.body.style.overflow = 'hidden';

    // Close binding
    const closeBtn = blogModal.querySelector('.course-modal-close');
    closeBtn.addEventListener('click', closeArticle);
    blogModal.addEventListener('click', (e) => {
      if (e.target === blogModal) closeArticle();
    });
  }

  function closeArticle() {
    if (!blogModal) return;
    blogModal.classList.remove('active');
    document.body.style.overflow = '';
  }
});

